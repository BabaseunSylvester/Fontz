fun transformText(input: String, mapping: List<String>): String {
    val sb = StringBuilder()
    input.forEach { char ->
        val index = standardChars.indexOf(char)
        if (index != -1 && index < mapping.size) {
            sb.append(mapping[index])
        } else {
            sb.append(char)
        }
    }
    return sb.toString()
}




